# Subsección: Ataques no provocados
st.markdown("**• Ataques no provocados**")

st.markdown("""
<div style='text-align: justify; line-height: 1.6; font-size: 16px;'>
    
Son iniciados por el tiburón; ocurren en el hábitat natural del tiburón contra un ser humano vivo y sin provocación del mismo. Hay tres subcategorías de ataques no provocados:

- Ataque de fuga: Generalmente no mortal, el tiburón muerde y se va; la mayoría de las víctimas no lo ven. Este es el tipo de ataque más común y suele ocurrir en la zona de rompientes o en aguas turbias. Se cree que la mayoría de los ataques de fuga se deben a una confusión de identidad por parte del tiburón.

- Ataque furtivo: La víctima no suele ver al tiburón y puede recibir múltiples mordeduras profundas. Este tipo de ataque es de naturaleza depredadora y suele llevarse a cabo con la intención de consumir a la víctima. Es extremadamente raro que esto ocurra.

- Ataque de choque y mordida: El tiburón rodea y golpea a la víctima antes de morderla. Se sabe que los grandes blancos hacen esto ocasionalmente, lo que se conoce como "mordida de prueba", en la que intentan identificar la presa. Las mordeduras repetidas, dependiendo de la reacción de la víctima (agitarse o entrar en pánico pueden hacer que el tiburón crea que es una presa), no son infrecuentes y pueden ser graves o mortales. No se cree que los ataques de choque y mordida sean resultado de una confusión de identidad.


</div>
""", unsafe_allow_html=True)

