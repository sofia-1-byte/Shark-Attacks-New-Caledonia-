# Sección: Tiburón
st.markdown("### Tiburón")

st.markdown("""
<div style='text-align: justify; line-height: 1.6; font-size: 16px;'>
    
Un tiburón es un pez cartilaginoso (perteneciente a la clase Chondrichthyes). Se caracterizan por un endoesqueleto sin costillas, dentículos dérmicos, de cinco a siete hendiduras branquiales a cada lado, aletas pectorales que no están fusionadas a la cabeza y escamas placoides.

Los tiburones varían en tamaño desde el pequeño tiburón linterna enano, una especie de aguas profundas que mide solo 17 centímetros de largo, hasta el tiburón ballena, siendo el pez más grande del mundo, que alcanza aproximadamente 12 metros de largo y puede pesar unas 21,5 toneladas.​ Se encuentran en todos los mares y son comunes a profundidades de hasta 2000 metros, aunque generalmente no viven en agua dulce, salvo algunas excepciones conocidas, como el tiburón sarda y los tiburones de río, que se pueden encontrar tanto en agua de mar como en agua dulce, mientras que el tiburón del Ganges que vive solo en agua dulce.

Los tiburones tienen una cubierta de escamas placoideas (dentículos) que protege la piel de daños y parásitos , además de mejorar su dinámica de fluidos . Tienen numerosos juegos de dientes reemplazables.


</div>
""", unsafe_allow_html=True)

